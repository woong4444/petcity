SELECT
    MEMBER_ID,
    LOGIN_ID,
    ROLE,
    STATUS,
    MEMBER_STATUS,
    HARD_DELETE_AT
FROM APP_MEMBER
WHERE ROLE != 'ADMIN'
  AND HARD_DELETE_AT IS NOT NULL
  AND HARD_DELETE_AT <= LOCALTIMESTAMP
  AND (
        MEMBER_STATUS = 'DELETE_PENDING'
        OR (
            STATUS = 'DELETED'
            AND MEMBER_STATUS = 'DELETED'
        )
      );



SELECT
    MEMBER_ID,
    STATUS,
    MEMBER_STATUS,
    HARD_DELETE_AT,
    CASE
        WHEN HARD_DELETE_AT <= LOCALTIMESTAMP THEN '삭제 기한 경과'
        ELSE '삭제 대기'
        END AS DELETE_RESULT
FROM APP_MEMBER
WHERE HARD_DELETE_AT IS NOT NULL
ORDER BY HARD_DELETE_AT;