UPDATE APP_MEMBER
SET
    STATUS = 'ACTIVE',
    MEMBER_STATUS = 'ACTIVE',
    LAST_LOGIN_AT = ADD_MONTHS(LOCALTIMESTAMP, -13),
    BLOCKED_AT = NULL,
    DELETE_REQUESTED_AT = NULL,
    DELETED_AT = NULL,
    HARD_DELETE_AT = NULL,
    DELETE_REASON = NULL
WHERE MEMBER_ID = 100
  AND ROLE != 'ADMIN';

COMMIT;



UPDATE APP_MEMBER
SET BLOCKED_AT = LOCALTIMESTAMP - INTERVAL '31' DAY
WHERE MEMBER_ID = 100;

COMMIT;



